# ===========================================================
# ================= ECG VECTOR BUILDER (MODLAR) ==============
# ===========================================================

def gaussian(t, mu, sigma, amp=1.0):
    return amp * np.exp(-0.5 * ((t - mu) / sigma) ** 2)

def unit(v):
    v = np.array(v, dtype=float)
    return v / np.linalg.norm(v)

def gate(t, t_start, t_end):
    return ((t >= t_start) & (t <= t_end)).astype(float)

def wave_windowed(t, mu, sigma, amp, direction, t_start, t_end):
    return np.outer(
        gaussian(t, mu, sigma, amp) * gate(t, t_start, t_end),
        unit(direction)
    )

# ============================================================
# COMPONENTS
# ============================================================

def p_wave(t):
    return wave_windowed(
        t, 0.16, 0.018, 0.10,
        [-0.4, 0.3, -0.85],
        0.10, 0.22
    )

def pr_segment(t):
    return np.zeros((len(t), 3))

def qrs_complex(t):
    gain = 0.75

    Q = wave_windowed(
        t, 0.34, 0.010,
        0.08 * gain,
        [0.9, 0.0, 0.1],
        0.30, 0.38
    )

    R = wave_windowed(
        t, 0.38, 0.015,
        1.00 * gain,
        [-0.7, 0.25, -0.65],
        0.34, 0.43
    )

    S = wave_windowed(
        t, 0.42, 0.012,
        0.15 * gain,
        [-0.3, -0.4, 0.7],
        0.38, 0.46
    )

    return Q + R + S

def st_segment(t, elevation=0.0, direction=[-0.2, 0.3, -0.9]):
    return np.outer(elevation * gate(t, 0.44, 0.56), unit(direction))

def t_wave(t):
    return wave_windowed(
        t, 0.70, 0.060, 0.25,
        [-0.6, 0.2, -0.6],
        0.58, 0.86
    )

# ============================================================
# FINAL VECTOR
# ============================================================

def build_vector(t):
    V = np.zeros((len(t), 3))
    V += p_wave(t)
    V += pr_segment(t)
    V += qrs_complex(t)
    V += st_segment(t)
    V += t_wave(t)
    return V

