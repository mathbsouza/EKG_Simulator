# ============================================================
# CAMPO 2D EM UM PLANO
# ============================================================

def generate_2d_plane(plane="frontal", resolution=600, max_radius=1.0, threshold=0.25):
    """
    plane = 'frontal'    -> Y = 0, retorna grade em X-Z
    plane = 'horizontal' -> Z = 0, retorna grade em X-Y
    """

    a = np.linspace(-max_radius, max_radius, resolution)
    b = np.linspace(-max_radius, max_radius, resolution)
    A, B = np.meshgrid(a, b)

    mask = np.full_like(A, np.nan, dtype=float)

    for i in range(resolution):
        for j in range(resolution):

            if plane == "frontal":
                x = A[i, j]
                y = 0.0
                z = B[i, j]
            elif plane == "horizontal":
                x = A[i, j]
                y = B[i, j]
                z = 0.0
            else:
                raise ValueError("plane must be 'frontal' or 'horizontal'")

            vec = np.array([x, y, z], dtype=float)
            r = np.linalg.norm(vec)

