# ============================================================
# FIGURA PUBLICÁVEL EM DOIS PLANOS
# ============================================================

def plot_stemi_2d_publication(resolution=600, max_radius=1.0, threshold=0.25,
                              savepath=None, dpi=600):
    """
    Gera figura 2D publicável com dois painéis:
      A) plano frontal   (X vs Z, Y=0)
      B) plano horizontal (X vs Y, Z=0)
    """

    Xf, Zf, Mf = generate_2d_plane(
        plane="frontal",
        resolution=resolution,
        max_radius=max_radius,
        threshold=threshold
    )

    Xh, Yh, Mh = generate_2d_plane(
        plane="horizontal",
        resolution=resolution,
        max_radius=max_radius,
        threshold=threshold
    )

    fig, axes = plt.subplots(1, 2, figsize=(10, 5), constrained_layout=True)

    panels = [
        (axes[0], Xf, Zf, Mf, "Frontal plane (Y = 0)", "X (Right–Left)", "Z (Superior–Inferior)"),
        (axes[1], Xh, Yh, Mh, "Horizontal plane (Z = 0)", "X (Right–Left)", "Y (Anterior–Posterior)")
    ]

    for ax, A, B, M, title, xlabel, ylabel in panels:
        # região positiva
        ax.contourf(
            A, B, M,
            levels=[0.5, 1.5],
            alpha=0.9
        )

        # borda da região STEMI
        ax.contour(
            A, B, M,
            levels=[0.5],
            linewidths=1.5
        )

        # borda da esfera seccionada
        circle = plt.Circle((0, 0), max_radius, fill=False, linewidth=1.0)
        ax.add_patch(circle)

        # eixos centrais
        ax.axhline(0, linewidth=0.8)
        ax.axvline(0, linewidth=0.8)

        ax.set_title(title, fontsize=11)
        ax.set_xlabel(xlabel, fontsize=10)
        ax.set_ylabel(ylabel, fontsize=10)

        ax.set_aspect("equal")
        ax.set_xlim(-max_radius, max_radius)
        ax.set_ylim(-max_radius, max_radius)

    if savepath is not None:
        plt.savefig(savepath, dpi=dpi, bbox_inches="tight")

    plt.show()


