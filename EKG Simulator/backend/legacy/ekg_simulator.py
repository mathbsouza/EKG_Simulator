def add_bullseye(vec, angles_basal, angles_mid, angles_apical):

    global fig

    fig.data = tuple(
        t for t in fig.data
        if getattr(t, "name", None) not in ("Bullseye", "Damage")
    )

    # =========================================================
    # GRID FIXO (ANATOMIA)
    # =========================================================

    theta = np.linspace(0, 2*np.pi, 360)
    r = np.linspace(0, 1, 160)

    T, R = np.meshgrid(theta, r)

    # 🔥 orientação fixa (hexaxial aproximado)
    rotation_offset = np.deg2rad(-30)
    T = np.mod(T + rotation_offset, 2*np.pi)

    scale = 0.9
    X = scale * R * np.cos(T)
    Y = scale * R * np.sin(T)

    # =========================================================
    # GEOMETRIA DO CONE
    # =========================================================

    z_basal, z_mid, z_apical = 0.25, -0.25, -0.90

    slope1 = (z_mid - z_apical) / 0.40
    slope2 = (z_basal - z_mid) / 0.30

    Z = np.zeros_like(R)

    Z[R < 0.40] = z_apical + slope1 * R[R < 0.40]
    Z[(R >= 0.40) & (R < 0.70)] = z_mid + slope2 * (R[(R >= 0.40) & (R < 0.70)] - 0.40)
    Z[R >= 0.70] = z_basal + slope2 * (R[R >= 0.70] - 0.70)

    # =========================================================
    # 🔥 MAPA DE DANO (FISIOLOGICAMENTE MELHOR)
    # =========================================================

    vec = np.array(vec, dtype=float)
    norm = np.linalg.norm(vec)

    if norm > 1e-6:
        v = -vec / norm

        # -------------------------
        # ÂNGULO NO PLANO FRONTAL
        # -------------------------
        theta_vec = np.arctan2(v[1], -v[0])

        # diferença angular
        dtheta = np.angle(np.exp(1j*(T - theta_vec)))

        # largura angular do dano
        sigma_theta = 0.5
        angular_component = np.exp(-(dtheta**2)/(2*sigma_theta**2))

        # -------------------------
        # COMPONENTE APICALIDADE
        # -------------------------
        # z positivo = basal, negativo = apical

        z_norm = (Z - Z.min()) / (Z.max() - Z.min())

        # vetor aponta para cima/baixo
        apical_bias = (1 - v[2]) / 2  # 0 basal → 1 apical

        sigma_z = 0.25
        radial_component = np.exp(-((z_norm - apical_bias)**2)/(2*sigma_z**2))

        # -------------------------
        # COMBINAÇÃO FINAL
        # -------------------------
        Z_damage = angular_component * radial_component
        Z_damage /= (Z_damage.max() + 1e-6)

        mask_damage = Z_damage > 0.55

    else:
        mask_damage = np.zeros_like(Z, dtype=bool)

    # =========================================================
    # SEGMENTAÇÃO
    # =========================================================

    colors = [
        "#8FAADC","#F6BD60","#F28482","#84A59D","#90BE6D","#F7EDE2",
        "#CDB4DB","#FFC8DD","#B5838D","#A0C4FF","#BDB2FF","#FFD6A5",
        "#CAFFBF","#FFADAD","#D0F4DE","#FDFFB6","#9BF6FF"
    ]

    segment_id = 0

    def angle_mask(a0, a1):
        if a1 < a0:
            return (T >= a0) | (T < a1)
        return (T >= a0) & (T < a1)

    def add_segment(r0, r1, angles):
        nonlocal segment_id

        for i in range(len(angles)):
            a0 = angles[i]
            a1 = angles[(i+1) % len(angles)]

            mask = (R >= r0) & (R < r1) & angle_mask(a0, a1)

            fig.add_trace(go.Surface(
                x=np.where(mask, X, np.nan),
                y=np.where(mask, Y, np.nan),
                z=np.where(mask, Z, np.nan),
                surfacecolor=np.ones_like(Z),
                colorscale=[[0, colors[segment_id]], [1, colors[segment_id]]],
                opacity=0.55,
                showscale=False,
                name="Bullseye"
            ))

            segment_id += 1

    add_segment(0.70,1.00,angles_basal)
    add_segment(0.40,0.70,angles_mid)
    add_segment(0.18,0.40,angles_apical)

    # apex
    fig.add_trace(go.Surface(
        x=np.where(R < 0.18, X, np.nan),
        y=np.where(R < 0.18, Y, np.nan),
        z=np.where(R < 0.18, Z, np.nan),
        surfacecolor=np.ones_like(Z),
        colorscale=[[0, colors[16]], [1, colors[16]]],
        opacity=0.55,
        showscale=False,
        name="Bullseye"
    ))

    # =========================================================
    # 🔥 DANO (overlay branco)
    # =========================================================

    fig.add_trace(go.Surface(
        x=np.where(mask_damage, X, np.nan),
        y=np.where(mask_damage, Y, np.nan),
        z=np.where(mask_damage, Z + 0.02, np.nan),
        surfacecolor=np.ones_like(Z),
        colorscale=[[0, "white"], [1, "white"]],
        opacity=0.9,
        showscale=False,
        name="Damage"
    ))

    # =========================================================
    # CONTORNOS + RADIAIS
    # =========================================================

    theta_lines = np.linspace(0,2*np.pi,500)

    def z_func(rval):
        if rval < 0.40:
            return z_apical + slope1 * rval
        elif rval < 0.70:
            return z_mid + slope2 * (rval - 0.40)
        return z_basal + slope2 * (rval - 0.70)

    for r_line, w in [(0.18,5),(0.40,5),(0.70,5),(1.00,6)]:
        z_val = z_func(r_line)
        fig.add_trace(go.Scatter3d(
            x=scale*r_line*np.cos(theta_lines),
            y=scale*r_line*np.sin(theta_lines),
            z=[z_val]*len(theta_lines),
            mode="lines",
            line=dict(color="black", width=w),
            showlegend=False
        ))

    def draw_radials(r0, r1, angles):
        for ang in angles:
            fig.add_trace(go.Scatter3d(
                x=[scale*r0*np.cos(ang), scale*r1*np.cos(ang)],
                y=[scale*r0*np.sin(ang), scale*r1*np.sin(ang)],
                z=[z_func(r0), z_func(r1)],
                mode="lines",
                line=dict(color="black", width=3),
                showlegend=False
            ))

    draw_radials(0.70,1.00,angles_basal)
    draw_radials(0.40,0.70,angles_mid)
    draw_radials(0.18,0.40,angles_apical)

