def add_ground_bullseye(vec, z_plane=-1.2):

    global fig

    # remove versões anteriores
    fig.data = tuple(
        t for t in fig.data
        if t.name not in ["GROUND_BULLSEYE", "GROUND_LABELS"]
    )

    # =========================================================
    # GRID POLAR
    # =========================================================

    theta = np.linspace(0, 2*np.pi, 300)
    r = np.linspace(0, 1, 120)

    T, R = np.meshgrid(theta, r)

    scale = 0.9

    X = scale * R * np.cos(T)
    Y = scale * R * np.sin(T)
    Z = np.full_like(X, z_plane)

    # =========================================================
    # NORMAL DO PLANO (para iluminação)
    # =========================================================

    normal = np.array([0, 0, 1])  # plano horizontal

    vec = np.array(vec, dtype=float)
    norm = np.linalg.norm(vec)

    if norm > 1e-6:
        v = vec / norm
        intensity = np.dot(normal, v)

        # projeção angular no plano
        vec_proj = np.array([v[0], v[1], 0])
        proj_norm = np.linalg.norm(vec_proj)

        if proj_norm > 1e-6:
            vec_proj /= proj_norm
            theta_vec = np.arctan2(vec_proj[1], vec_proj[0])
        else:
            theta_vec = 0

        dtheta = np.angle(np.exp(1j * (T - theta_vec)))

        sigma = 0.5
        Z_heat = np.exp(-(dtheta**2)/(2*sigma**2))

        # modula pela incidência
        Z_heat *= max(0, intensity)

    else:
        Z_heat = np.zeros_like(X)

    # =========================================================
    # SUPERFÍCIE (heatmap branco)
    # =========================================================

    fig.add_trace(go.Surface(
        x=X,
        y=Y,
        z=Z,
        surfacecolor=Z_heat,
        colorscale=[[0, "rgba(0,0,0,0)"], [1, "white"]],
        showscale=False,
        opacity=1.0,
        name="GROUND_BULLSEYE"
    ))

    # =========================================================
    # SEGMENTAÇÃO AHA
    # =========================================================

    rings = [0.18, 0.40, 0.70, 1.00]

    angles_basal = np.deg2rad([0, 60, 120, 180, 240, 300])
    angles_mid   = np.deg2rad([0, 60, 120, 180, 240, 300])
    angles_apical= np.deg2rad([45, 135, 225, 315])

    # círculos
    theta_lines = np.linspace(0, 2*np.pi, 400)

    for r_line in rings:
        fig.add_trace(go.Scatter3d(
            x=scale * r_line * np.cos(theta_lines),
            y=scale * r_line * np.sin(theta_lines),
            z=[z_plane]*len(theta_lines),
            mode="lines",
            line=dict(color="black", width=3),
            showlegend=False
        ))

    # radiais
    def draw_radials(r0, r1, angles):
        for ang in angles:
            fig.add_trace(go.Scatter3d(
                x=[scale*r0*np.cos(ang), scale*r1*np.cos(ang)],
                y=[scale*r0*np.sin(ang), scale*r1*np.sin(ang)],
                z=[z_plane, z_plane],
                mode="lines",
                line=dict(color="black", width=2),
                showlegend=False
            ))

    draw_radials(0.70, 1.00, angles_basal)
    draw_radials(0.40, 0.70, angles_mid)
    draw_radials(0.18, 0.40, angles_apical)


