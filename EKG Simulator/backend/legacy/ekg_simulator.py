# CRITÉRIO DE STEMI (GENERALIZADO)
# ============================================================

def is_stemi(vec, groups, threshold=0.25):
    proj = project_vector(vec)

    for group in groups:
        count = sum(proj[l] > threshold for l in group)
        if count >= 2:
            return True

    return False


# ============================================================
# CAMPO ESCALAR CONTÍNUO
# ============================================================

def generate_scalar_field(groups, resolution=35, max_radius=1.0, threshold=0.25):

    xs = np.linspace(-max_radius, max_radius, resolution)
    ys = np.linspace(-max_radius, max_radius, resolution)
    zs = np.linspace(-max_radius, max_radius, resolution)

    X, Y, Z = np.meshgrid(xs, ys, zs, indexing="ij")
    values = np.zeros_like(X)

    for i in range(resolution):
        for j in range(resolution):
            for k in range(resolution):

                vec = np.array([X[i, j, k], Y[i, j, k], Z[i, j, k]])
                r = np.linalg.norm(vec)

                if r == 0 or r > max_radius:
                    continue

                values[i, j, k] = 1 if is_stemi(vec, groups, threshold) else 0

    return X, Y, Z, values


